<h1>Acerca del sistema</h1>	

<p>Información general del sistema</p>

<div class="px-3">
  
  <p>
  	El sistema de inscripción de estudiantes está diseñado para permitir un flujo de trabajo más eficiente así como de mantener la información almacenada de mejor manera. Por lo tanto cuenta con una serie de funciones que permiten que estos objetivos como: registrar a los estudiantes con todos sus datos pertinentes junto con los datos de su representante y padres; consultar los datos de los estudiantes ya inscritos, general reportes generales de los estudiantes, padres y representantes y las planillas de inscripción correspondientes; asimismo, se puede consultar las estadísticas de los datos almacenados, cabe destacar que los reportes generales, las consultas y las estadísticas cuentan con un filtro que les permite a los usuarios escoger la información que necesitan en el momento; también el sistema permite al administrador generar respaldos de la base de datos así como restaurarla de ser necesario.
  </p>

  <p>
  	El mantenimiento del sistema será realizado por los técnicos competentes y se manejará de acuerdo a la necesidad del mismo.
  </p>

  <p>
  	El sistema cumple con los estándares de seguridad y, a su vez, los usuarios deben cumplir con las políticas de seguridad establecidas por los desarrolladores.
  </p>

</div>

