		<h4>
			<u>
				Acerca del módulo.
			</u>
		</h4>


		<p>
			Al ingresar a la plataforma del sistema se encontrará en primer lugar con la pantalla de inicio de sesión
			(<a href="#imagen1"><i>Ver imagen 1</i></a>), en esta pantalla se encontrará con un breve formulario con 3 campos, estos campos son los siguientes:
		</p>


		<ol>
			<li>Nacionalidad: es un selector desplegable y sus posibles opciones elegibles son <i>Venezolano(a)</i> ó <i>Extranjero(a)</i>.</li>
			<li>Número de cédula: dígitos que componen la cédula del usuario (excluyendo los caracteres V o E de la nacionalidad).</li>
			<li>Contraseña: contraseña del usuario con el que se desea ingresar.</li>
		</ol>


		<p>
			Una vez ingresados los datos que se solicitan, presione el botón <i>Ingresar al sistema</i>, o en caso de que no recuerde su contraseña, presione el enlace de <i>¿Contraseña olvidada?</i> (<a href="?con=a2"><i>Véase tópico de inicio auxiliar</i></a>)
		</p>


		<p>
			Debe tener en consideración que para hacer uso de las funciones del sistema, el administrador debe haberle asignado un usuario con el cual trabajar. Ya que los datos ingresados serán validados, si estos coinciden con su usuario será enviado al menú principal, en caso contrario, será retornado a la pantalla de inicio de sesión con un mensaje que indica que los datos no son correctos (No se especifica cuál es correcto y cuál no)(<a href="#imagen2"><i>Ver imagen 2</i></a>).
		</p>


		<p>
			Cabe mencionar que mientras se llena el formulario, se resaltarán los campos; si el campo cumple con lo requisitos, se resaltará de color verde, en caso contrario se resaltará en color rojo, así como también se mostrará una leyenda indicando el error cometido. 
			Véase (
			<a href="#imagen3"><i>Imagen 3</i></a>
			<a href="#imagen4"><i>Imagen 4</i></a>
			<a href="#imagen5"><i>Imagen 5</i></a>).
		</p>


